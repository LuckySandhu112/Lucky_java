import java.util.*;
public class PoNeZero
{
    public static void main(String args[])
    {
        int num=-1;
        if(num>0)
        {
            System.out.println("It is a positive number: "+ num);
        }
        else if(num<0)
        {
            System.out.println("It is a negative number: "+ num);
        }
        else if(num==0)
        {
            System.out.println("It is zero: "+ num);
        }
        else
        {
            System.out.println("The number is incorrect: "+ num);
        }
    }
}
