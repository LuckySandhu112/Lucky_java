import java.util.*;
public class EvenOdd
{
    public static void main(String args[])
    {
        int num = 3;

        if(num%2==0)
        {
            System.out.println("It is an Even number " + num);
        }
        else
        {
            System.out.println("It is an Odd number " + num);
        }
    }
}
